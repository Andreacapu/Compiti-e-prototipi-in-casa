print("lista originale", lista_località)

print("lista in ordine alfabetico",sorted(lista_località))

print("lista in ordine originale dopo la modifica",sorted(lista_località, reverse = True))

print("lista originale dopo la seconda inversione():", lista_località)

print("lista in ordine invertito", lista_località[::-1])


